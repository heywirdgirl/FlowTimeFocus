function useToast() {
  const router = useRouter(); // L·∫•y router ƒë·ªÉ d√πng trong prompt
  const [state, setState] = React.useState<State>(memoryState)

  React.useEffect(() => {
    listeners.push(setState)
    return () => {
      const index = listeners.indexOf(setState)
      if (index > -1) {
        listeners.splice(index, 1)
      }
    }
  }, [state])

  return {
    ...state,
    toast,
    dismiss: (toastId?: string) => dispatch({ type: "DISMISS_TOAST", toastId }),
    showLoginPrompt: (action: string) => showLoginPrompt(action, router), // Export h√†m prompt
  }
}

